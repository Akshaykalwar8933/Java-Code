import java.util.*;
public class if_else_are_both {
    public static void main(String[]args){
        Scanner sc = new Scanner (System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        if (a == b){
            System.out.println(" a is equal");
        }else if( a > b){
                System.out.println("a is greater");
              }else{
                System.out.println("a is lesser");
              }
        }
    }        
    


